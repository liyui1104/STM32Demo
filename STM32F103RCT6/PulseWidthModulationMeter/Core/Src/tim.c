/**
 * @brief  Initialize TIM2 for PWM output on all four channels.
 *         This function starts PWM generation on channels 1, 2, 3, and 4 of TIM2.
 *         It also sets the default frequency of the PWM signal.
 * @param  None
 * @retval None
 * @note   The PWM frequency is set to 50 Hz initially. The prescaler is adjusted
 *         based on the desired frequency and the timer clock source (72 MHz).
 *         After calling this function, PWM outputs are enabled on TIM2 channels.
 */
void TIM2_PWM_Init(void)
{
  // Set the frequency of PWM Output signal
  frequency = 50;
  __HAL_TIM_SET_PRESCALER(&htim2, (72000000 / (frequency * 2000)) - 1);

  HAL_TIM_PWM_Start(&htim2, TIM_CHANNEL_1);
  HAL_TIM_PWM_Start(&htim2, TIM_CHANNEL_2);
  HAL_TIM_PWM_Start(&htim2, TIM_CHANNEL_3);
  HAL_TIM_PWM_Start(&htim2, TIM_CHANNEL_4);
}

/**
 * @brief Input Capture callback for TIM3. This function is called when a capture event occurs on TIM3.
 *        It calculates the frequency and duty ratio of the input PWM signal.
 * @param htim Pointer to the TIM handle structure.
 * @retval None
 */
void HAL_TIM_IC_CaptureCallback(TIM_HandleTypeDef *htim)
{
  // Check if the capture event occurred on Channel 1 of TIM3
  if (htim->Channel == HAL_TIM_ACTIVE_CHANNEL_1)
  {
    // Read the captured value on the rising edge (Channel 1)
    IC1_value = HAL_TIM_ReadCapturedValue(htim, TIM_CHANNEL_1);

    // Read the captured value on the falling edge (Channel 2)
    IC2_value = HAL_TIM_ReadCapturedValue(htim, TIM_CHANNEL_2);

    // Ensure IC1_value is not zero to prevent division by zero
    if (IC1_value != 0)
    {
      // Calculate the duty ratio as a percentage
      input_duty_ratio = ((IC2_value + 1) * 100) / IC1_value + 1;

      // Calculate the frequency of the PWM signal
      input_frequency = 1000000 / IC1_value;
    }
  }
}

